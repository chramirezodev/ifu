# Crear proyecto Next.js con TypeScript y TailwindCSS
npx create-next-app@latest immigration-for-us --typescript --tailwind --eslint

# Entrar al directorio del proyecto
cd immigration-for-us

# Instalar dependencias necesarias
npm install framer-motion react-intersection-observer
npm install @sanity/client next-sanity @portabletext/react
npm install -D @tailwindcss/typography @types/nodemailer 